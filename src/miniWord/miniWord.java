    private void JPG2_decriptCLICKNO(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JPG2_decriptCLICKNO
        String text = JPG2_area.getText().trim();
        String combo = (String) JPG2_combo.getSelectedItem();
        String newText = "";
        try {
            newText = PersonalFile.decriptContent("jujuKeyPrivate", combo, text);
            JPG2_area.setText(newText);
        } catch (Exception ex) {            
            JOptionPane.showMessageDialog(null, "Error al desencriptar");
        }
    }//GEN-LAST:event_JPG2_decriptCLICKNO

    private void JPG2_criptCLICKNO(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JPG2_criptCLICKNO
        String combo = (String) JPG2_combo.getSelectedItem();
        try {
            String text = JPG2_area.getText().trim();
            String newText = PersonalFile.encriptContent("jujuKeyPrivate", combo, text); 
            JPG2_area.setText(newText);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al encriptar");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_JPG2_criptCLICKNO

